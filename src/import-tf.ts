// Converts Terraform HCL resource blocks to aglang node declarations.
// Uses regex parsing — handles only top-level resource blocks; skips modules.
// Supported providers: AWS, Google Cloud, Azure.

export interface TerraformImportResult {
  ag: string;
  nodes: number;
  skipped: number;
}

