function toPascalCase(s: string): string {
  return s.replace(/[-_]+(.)/g, (_: string, c: string) => c.toUpperCase())
          .replace(/^(.)/, (_: string, c: string) => c.toUpperCase());
}

const RESOURCE_RE = /resource\s+"([^"]+)"\s+"([^"]+)"\s*\{/g;

export function importTerraform(hcl: string): TerraformImportResult {
  const lines: string[] = [
    `// Generated by aglc import-tf`,
    `// Source: Terraform HCL`,
    `// Note: Only top-level resource blocks are imported; modules are skipped.`,
    ``,
  ];

  let nodeCount = 0;
  let skippedCount = 0;
  let m: RegExpExecArray | null;

  while ((m = RESOURCE_RE.exec(hcl)) !== null) {
    const tfType = m[1]!;
    const tfName = m[2]!;

    const nodeType = TF_TYPE_MAP[tfType];
    if (!nodeType) {
      skippedCount++;
      continue;
    }

    const nodeName = toPascalCase(tfName);
    lines.push(`node ${nodeName} : ${nodeType} {`);
    lines.push(`  // terraform: ${tfType}.${tfName}`);
    lines.push(`}`);
    lines.push(``);
    nodeCount++;
  }

  if (nodeCount === 0 && skippedCount === 0) {
    lines.push(`// No recognized resource blocks found.`);
  }

  return {
    ag: lines.join('\n'),
    nodes: nodeCount,
    skipped: skippedCount,
  };
}
